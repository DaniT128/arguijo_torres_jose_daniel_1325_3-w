def mayor_de_tres(num1, num2, num3):
    """Devuelve el mayor de tres números."""
    return max(num1, num2, num3)

# Ejemplo de uso
numero1 = 15
numero2 = 50
numero3 = 35

mayor = mayor_de_tres(numero1, numero2, numero3)
print(f"El mayor de los tres números es: {mayor}")
